- (void)tableView:(UITableView *)tableView commitEditingStyle:(UITableViewCellEditingStyle)editingStyle forRowAtIndexPath:(NSIndexPath *)indexPath
{
    NSManagedObjectContext *context = [self managedObjectContext];
    
    if (editingStyle == UITableViewCellEditingStyleDelete) {
        // Delete object from database
        [context deleteObject:[self.giftcards objectAtIndex:indexPath.row]];
        
        NSError *error = nil;
        if (![context save:&error]) {
            NSLog(@"Can't Delete! %@ %@", error, [error localizedDescription]);
            return;
        }
        
        // Remove device from table view
        [self.giftcards removeObjectAtIndex:indexPath.row];
        [self.tableView deleteRowsAtIndexPaths:[NSArray arrayWithObject:indexPath] withRowAnimation:UITableViewRowAnimationFade];
    }
}

- (void)prepareForSegue:(UIStoryboardSegue *)segue sender:(id)sender
{
    if ([[segue identifier] isEqualToString:@"toshowgcview"]) {
        NSManagedObject *selectedCard = [self.giftcards objectAtIndex:[[self.tableView indexPathForSelectedRow] row]];
        
        rowindexselected = self.tableView.indexPathForSelectedRow;
        
        //[[self.tableView cellForRowAtIndexPath:[self.tableView indexPathForSelectedRow]] setBackgroundColor:[UIColor lightGrayColor]];
        
        //[[[self.tableView objectAtIndex:indexPathForSelectedRow] row] setBackgroundColor:[UIColor blueColor]];
        
        ShowGCViewController *destViewController = segue.destinationViewController;
        destViewController.giftcard = selectedCard;
    }
}

